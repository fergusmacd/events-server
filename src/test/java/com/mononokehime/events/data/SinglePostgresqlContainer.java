import org.testcontainers.containers.PostgreSQLContainer;

public class SinglePostgresqlContainer extends PostgreSQLContainer<SinglePostgresqlContainer> {
    private static final String IMAGE_VERSION = "postgres:11.1";
    private static SinglePostgresqlContainer container;

    private SinglePostgresqlContainer() {
        super(IMAGE_VERSION);
    }

    public static SinglePostgresqlContainer getInstance() {
        if (container == null) {
            container = new SinglePostgresqlContainer();
        }
        return container;
    }

    @Override
    public void start() {
        super.start();
        System.setProperty("DB_URL", container.getJdbcUrl());
        System.setProperty("DB_USERNAME", container.getUsername());
        System.setProperty("DB_PASSWORD", container.getPassword());
    }

    @Override
    public void stop() {
        //do nothing, JVM handles shut down
    }
}
